-- 20260620_patient_doctor_diagnosis.sql
-- Аддитивно: отдельное поле «диагноз от врача» в карточке пациента.
--
-- Контекст позиционирования: Azarean — wellness-сервис восстановления, НЕ ЭМК и
-- НЕ медорганизация. Это поле — просто свободный текст того, что пациенту поставил
-- ВНЕШНИЙ врач (направление/заключение), которое инструктор переносит в карточку.
-- Оно отделено от рабочего `patients.diagnosis` (диагноз/проблема под программу),
-- чтобы не смешивать формулировку врача с внутренней рабочей пометкой.
--
-- Хранится plaintext, как и остальные поля (diagnosis/notes/diary) — at-rest
-- шифрование спец-категории ПДн остаётся в backlog и не вводится этой миграцией.
-- Полностью идемпотентна.

ALTER TABLE patients
  ADD COLUMN IF NOT EXISTS doctor_diagnosis TEXT;

COMMENT ON COLUMN patients.doctor_diagnosis IS
  'Диагноз от внешнего врача (свободный текст, заносит инструктор). Отдельно от рабочего diagnosis.';
