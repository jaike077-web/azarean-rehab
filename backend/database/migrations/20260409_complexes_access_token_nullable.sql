-- Часть плана миграции от "публичных ссылок /patient/:token"
-- к единому личному кабинету пациента.
--
-- Этап 1: снимаем NOT NULL constraint, чтобы POST /complexes
-- мог создавать записи без генерации access_token.
-- UNIQUE constraint и индекс idx_complexes_token остаются —
-- будут удалены в Этапе 6 вместе с самой колонкой.
--
-- После этой миграции новые комплексы создаются с access_token = NULL,
-- старые данные не трогаются (UNIQUE допускает множественные NULL в PostgreSQL).
-- =====================================================

ALTER TABLE complexes ALTER COLUMN access_token DROP NOT NULL;
