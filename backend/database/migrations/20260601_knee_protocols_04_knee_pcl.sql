-- Knee protocols, коммит S3 (ТЗ TZ_PATIENT_PATH_KNEE_PROTOCOLS.md §7), по образцу knee_oa-seed.
--
-- Источник: PROTOCOLS/_extractions.json (объект suggested_program_type_code='knee_pcl').
-- В досье ДВЕ траектории (консервативная Grade I-II + послеоперационная Grade III/сочетанные).
-- В извлечении phases описана ПОСЛЕОПЕРАЦИОННАЯ ветвь (4 фазы, наиболее детальная) — её и
-- сидируем. Консервативная ветвь и отдельный program_type (knee_pcl_conservative) — отложены
-- до отдельной работы, как и prehab у других протоколов (слот D3).
--
-- Нумерация фаз С 1 (в извлечении фазы 0 нет — этапа «оценка/вход» не было). 4 фазы:
-- 1 Защита связки → 2 Возвращаем движение и силу → 3 Сила и бег → 4 Возврат к спорту.
-- Контент в ПАЦИЕНТСКОМ регистре (без клинич. жаргона/цитат) — Vadim калибрует через AdminContent.
--
-- criteria_next (free-text на rehab_phases) дублирует метки критериев → пациент видит их СРАЗУ
-- в «Пути». Структурированная phase_transition_criteria — для будущей авто-проверки (слот D2).
--
-- Классификация критериев (ТЗ §2.3): criterion_type='measurement' ТОЛЬКО когда систему сегодня
-- можно проверить автоматически против хранимых замеров — сгибание/разгибание колена (rom),
-- боль ВАШ (vas_score / pain), обхват голени (girth). Для ЗКС это сгибание колена (Фаза 1).
-- ВСЕ остальные числовые пороги (сила квадрицепса/хамстрингов LSI %, hop-тесты, баллы опросников
-- IKDC/Lysholm, задняя слабина в мм) система НЕ хранит → 'instructor_check' с числовым порогом,
-- ВПИСАННЫМ В ТЕКСТ label (куратор вводит очно). «Полный объём движений» без конкретного градуса —
-- тоже instructor_check (нет порога на сгибание/разгибание в градусах). Качественное «нет боли»
-- без числа → self_report.
--
-- ВАЖНО (клинический caveat из досье): PCL-специфичных валидированных RTS-критериев нет — пороги
-- LSI ≥90% / hop >90% / IKDC >90 / Lysholm >85 экстраполированы с ПКС и являются ОРИЕНТИРАМИ под
-- калибровку куратора/хирурга. Многие ранние сроки и режимы (нагрузка, ортез, ввод хамстрингов)
-- задаёт оперирующий хирург — здесь не зашиты жёстко.
--
-- Идемпотентна: ON CONFLICT DO NOTHING для обеих таблиц (AdminContent = источник истины после
-- деплоя; ручная калибровка не затирается). Прогон тест-цикла createdb→schema→migrations×2 → ок.
-- НЕ трогает acl/knee_oa/другие program_type (разные program_type/phase_id).

