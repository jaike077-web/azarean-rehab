-- с порогом в тексте label (куратор вводит очно). Качественные да/нет от пациента → 'self_report'.
--
-- Идемпотентна: ON CONFLICT DO NOTHING для обеих таблиц (AdminContent = источник истины после
-- деплоя; ручная калибровка не затирается). Прогон тест-цикла createdb→schema→migrations×2 → ок.
